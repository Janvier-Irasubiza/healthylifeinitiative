<script>
document.addEventListener('DOMContentLoaded', function() {
    let sections = document.querySelectorAll('.abt-sect-desc > div');
    let navLinks = document.querySelectorAll('.abt-list');

    let abtSectDesc = document.querySelector('.abt-sect-desc');

    abtSectDesc.addEventListener('scroll', function() {
        let currentSection = '';

        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            const scrollTop = abtSectDesc.scrollTop;

            if (scrollTop >= (sectionTop - sectionHeight / 0.7)) {
                currentSection = section.getAttribute('id');
            }
        });

        navLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href').substring(1) === currentSection) {
                link.classList.add('active');
            }
        });
    });
});
</script>

